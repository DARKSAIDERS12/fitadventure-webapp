        function addProductToMeal(productName, productData) {
            const product = {
                name: productName,
                amount: 100, // По умолчанию 100г
                calories: productData.калории,
                protein: productData.белки,
                fat: productData.жиры,
                carbs: productData.углеводы
            };
            
            mealPlan[currentMealType].push(product);
            saveMealPlan();
            renderMealSections();
            updateDailyStats();
            closeProductSelector();
        }

        function removeProductFromMeal(mealType, index) {
            mealPlan[mealType].splice(index, 1);
            saveMealPlan();
            renderMealSections();
            updateDailyStats();
        }

        function updateProductAmount(mealType, index, newAmount) {
            const amount = parseFloat(newAmount) || 100;
            const product = mealPlan[mealType][index];
            const multiplier = amount / 100;
            
            // Находим оригинальные данные продукта
            let originalData = null;
            for (const [category, goals] of Object.entries(productsDatabase)) {
                if (goals[currentGoal] && goals[currentGoal][product.name]) {
                    originalData = goals[currentGoal][product.name];
                    break;
                }
            }
            
            if (originalData) {
                product.amount = amount;
                product.calories = Math.round(originalData.калории * multiplier);
                product.protein = Math.round(originalData.белки * multiplier * 10) / 10;
                product.fat = Math.round(originalData.жиры * multiplier * 10) / 10;
                product.carbs = Math.round(originalData.углеводы * multiplier * 10) / 10;
            }
            
            saveMealPlan();
            renderMealSections();
            updateDailyStats();
        }

        function updateDailyStats() {
            let totalCalories = 0;
            let totalProteins = 0;
            let totalFats = 0;
            let totalCarbs = 0;
            
            Object.values(mealPlan).forEach(products => {
                products.forEach(product => {
                    totalCalories += product.calories;
                    totalProteins += product.protein;
                    totalFats += product.fat;
                    totalCarbs += product.carbs;
                });
            });
            
            document.getElementById('totalCalories').textContent = Math.round(totalCalories);
            document.getElementById('totalProteins').textContent = Math.round(totalProteins * 10) / 10 + 'г';
            document.getElementById('totalFats').textContent = Math.round(totalFats * 10) / 10 + 'г';
            document.getElementById('totalCarbs').textContent = Math.round(totalCarbs * 10) / 10 + 'г';
        }

