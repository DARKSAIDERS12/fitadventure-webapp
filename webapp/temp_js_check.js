        function applyFilters() {
            const calorieFilter = document.getElementById('calorieFilter').value;
            const proteinFilter = document.getElementById('proteinFilter').value;
            const carbsFilter = document.getElementById('carbsFilter').value;
            const fatFilter = document.getElementById('fatFilter').value;
            const sortFilter = document.getElementById('sortFilter').value;

            let filteredProducts = {};

            // Если есть текущая категория, фильтруем только её продукты
            if (currentCategory && productsDatabase[currentCategory]?.[currentGoal]) {
                const products = productsDatabase[currentCategory][currentGoal];
                for (const [name, data] of Object.entries(products)) {
                    if (passesFilters(data, calorieFilter, proteinFilter, carbsFilter, fatFilter)) {
                        filteredProducts[name] = data;
                    }
                }
            } else {
                // Если нет текущей категории, фильтруем все продукты для текущей цели
                for (const [category, goals] of Object.entries(productsDatabase)) {
                    if (goals[currentGoal]) {
                        for (const [name, data] of Object.entries(goals[currentGoal])) {
                            if (passesFilters(data, calorieFilter, proteinFilter, carbsFilter, fatFilter)) {
                                filteredProducts[name] = data;
                            }
                        }
                    }
                }
            }

            // Применяем фильтр избранного
            if (showFavoritesOnly) {
                const favoriteProducts = {};
                for (const [name, data] of Object.entries(filteredProducts)) {
                    if (favorites.includes(name)) {
                        favoriteProducts[name] = data;
                    }
                }
                filteredProducts = favoriteProducts;
            }

            // Сортируем продукты
            const sortedProducts = sortProducts(filteredProducts, sortFilter);

            // Отображаем отфильтрованные продукты
            displayProducts(sortedProducts);
        }

        function passesFilters(data, calorieFilter, proteinFilter, carbsFilter, fatFilter) {
            // Фильтр по калориям
            if (calorieFilter === 'low' && data.калории > 100) return false;
            if (calorieFilter === 'medium' && (data.калории <= 100 || data.калории > 300)) return false;
            if (calorieFilter === 'high' && data.калории <= 300) return false;

            // Фильтр по белкам
            if (proteinFilter === 'high' && data.белки < 15) return false;
            if (proteinFilter === 'medium' && (data.белки < 5 || data.белки >= 15)) return false;
            if (proteinFilter === 'low' && data.белки >= 5) return false;

            // Фильтр по углеводам
            if (carbsFilter === 'low' && data.углеводы > 10) return false;
            if (carbsFilter === 'medium' && (data.углеводы <= 10 || data.углеводы > 30)) return false;
            if (carbsFilter === 'high' && data.углеводы <= 30) return false;

            // Фильтр по жирам
            if (fatFilter === 'low' && data.жиры > 5) return false;
            if (fatFilter === 'medium' && (data.жиры <= 5 || data.жиры > 20)) return false;
            if (fatFilter === 'high' && data.жиры <= 20) return false;

            return true;
        }

        function sortProducts(products, sortBy) {
            const sortedArray = Object.entries(products);
            
            sortedArray.sort((a, b) => {
                const [nameA, dataA] = a;
                const [nameB, dataB] = b;
                
                switch (sortBy) {
                    case 'calories':
                        return dataB.калории - dataA.калории;
                    case 'protein':
                        return dataB.белки - dataA.белки;
                    case 'carbs':
                        return dataB.углеводы - dataA.углеводы;
                    case 'fat':
                        return dataB.жиры - dataA.жиры;
                    case 'name':
                    default:
                        return nameA.localeCompare(nameB);
                }
            });
            
            return Object.fromEntries(sortedArray);
        }

